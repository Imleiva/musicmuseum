// Función helper para obtener fact aleatorio
export const getRandomFact = (bandKey, language = "es") => {
  const bandFacts = facts[language]?.[bandKey];
  if (!bandFacts || bandFacts.length === 0) {
    return language === "es"
      ? "¡Esta banda tiene una historia fascinante por descubrir!"
      : "This band has a fascinating history to discover!";
  }
  const randomIndex = Math.floor(Math.random() * bandFacts.length);
  return bandFacts[randomIndex];
};

// Función helper para obtener todos los facts de una banda
export const getAllFacts = (bandKey, language = "es") => {
  return facts[language]?.[bandKey] || [];
};

// Función helper para buscar facts por palabra clave
export const searchFacts = (keyword, language = "es") => {
  const results = [];
  const languageFacts = facts[language] || {};
  Object.keys(languageFacts).forEach((bandKey) => {
    languageFacts[bandKey].forEach((fact) => {
      if (fact.toLowerCase().includes(keyword.toLowerCase())) {
        results.push({ bandKey, fact });
      }
    });
  });
  return results;
};
